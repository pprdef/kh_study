package kh_study3exam.practice_exam01;

public class Calc {
	public void calc(int a, int b) {
		System.out.println(a + " + " + b + " = " +(a+b));
		System.out.println(a + " - " + b + " = " +(a-b));
		System.out.println(a + " * " + b + " = " +(a*b));
		System.out.println(a + " / " + b + " = " +(a/b));
	}
}
